import { useState, useEffect } from 'react';

const FlashOfferTimer = ({ subtotal, products, settings, addToCart, cart }) => {
  const [timeLeft, setTimeLeft] = useState(0);
  const [isActive, setIsActive] = useState(false);
  const [hasTriggered, setHasTriggered] = useState(false);

  // –ù–∞—Ö–æ–¥–∏–º —Ç–æ–≤–∞—Ä —Å R2000 –≤ ID (—ç—Ç–æ –±—É–¥–µ—Ç "6R2000")
  const specialProduct = products.find(p => String(p.id).includes('R2000'));
  
  // –ü—Ä–æ–≤–µ—Ä—è–µ–º, –µ—Å—Ç—å –ª–∏ —É–∂–µ —ç—Ç–æ—Ç flash-—Ç–æ–≤–∞—Ä –≤ –∫–æ—Ä–∑–∏–Ω–µ
  const flashItem = cart.find(item => item.id === `${specialProduct?.id}_flash`);
  const isInCart = !!flashItem;

  // –ü—Ä–æ—Å—Ç–∞—è –ª–æ–≥–∏–∫–∞ –∞–∫—Ç–∏–≤–∞—Ü–∏–∏ –ø—Ä–µ–¥–ª–æ–∂–µ–Ω–∏—è
  useEffect(() => {
    if (!specialProduct) return;

    const shouldShow = subtotal >= 2000 && !isInCart && !hasTriggered;
    
    if (shouldShow) {
      setTimeLeft(120);
      setIsActive(true);
      setHasTriggered(true);
    }
    
    // –°–∫—Ä—ã–≤–∞–µ–º –µ—Å–ª–∏ —Å—É–º–º–∞ —É–ø–∞–ª–∞ –∏–ª–∏ —Ç–æ–≤–∞—Ä –¥–æ–±–∞–≤–ª–µ–Ω
    if ((subtotal < 2000 || isInCart) && isActive) {
      setIsActive(false);
      setTimeLeft(0);
    }
  }, [subtotal, specialProduct, isInCart, hasTriggered, isActive]);

  useEffect(() => {
    let interval = null;
    if (isActive && timeLeft > 0) {
      interval = setInterval(() => {
        setTimeLeft(timeLeft => {
          if (timeLeft <= 1) {
            setIsActive(false);
            return 0;
          }
          return timeLeft - 1;
        });
      }, 1000);
    } else if (timeLeft === 0) {
      setIsActive(false);
    }
    return () => clearInterval(interval);
  }, [isActive, timeLeft]);

  // –ù–µ –ø–æ–∫–∞–∑—ã–≤–∞–µ–º, –µ—Å–ª–∏ –Ω–µ—Ç —Å–ø–µ—Ü–∏–∞–ª—å–Ω–æ–≥–æ —Ç–æ–≤–∞—Ä–∞ –∏–ª–∏ –ø—Ä–µ–¥–ª–æ–∂–µ–Ω–∏–µ –Ω–µ–∞–∫—Ç–∏–≤–Ω–æ
  if (!specialProduct || !isActive || timeLeft <= 0 || isInCart) return null;

  const minutes = Math.floor(timeLeft / 60);
  const seconds = timeLeft % 60;
  const originalPrice = specialProduct.price;
  const discountedPrice = Math.round(originalPrice * 0.01); // 99% —Å–∫–∏–¥–∫–∞

  return (
    <div style={{
      background: 'linear-gradient(135deg, #ff0844, #ffb199)',
      color: 'white',
      padding: '1rem',
      borderRadius: '12px',
      marginBottom: '1rem',
      border: '2px solid #ffd700',
      boxShadow: '0 4px 15px rgba(255, 8, 68, 0.3)',
      animation: 'flashPulse 2s infinite',
      position: 'relative',
      overflow: 'hidden',
    }}>
      <style>
        {`
          @keyframes flashPulse {
            0%, 100% { transform: scale(1); }
            50% { transform: scale(1.01); }
          }
          
          @keyframes timerBlink {
            0%, 100% { opacity: 1; }
            50% { opacity: 0.7; }
          }
        `}
      </style>

      {/* –ö–æ–º–ø–∞–∫—Ç–Ω—ã–π –∑–∞–≥–æ–ª–æ–≤–æ–∫ */}
      <div style={{ 
        display: 'flex', 
        justifyContent: 'space-between', 
        alignItems: 'center',
        marginBottom: '0.75rem'
      }}>
        <div style={{ 
          fontSize: '1rem', 
          fontWeight: 'bold',
          textShadow: '1px 1px 2px rgba(0,0,0,0.3)'
        }}>
          ‚ö° –ú–û–õ–ù–ò–ï–ù–û–°–ù–û–ï –ü–†–ï–î–õ–û–ñ–ï–ù–ò–ï
        </div>
        <div style={{
          fontSize: '1.5rem',
          fontWeight: 'bold',
          fontFamily: 'monospace',
          animation: timeLeft <= 30 ? 'timerBlink 1s infinite' : 'none',
          color: timeLeft <= 30 ? '#ffff00' : '#ffffff',
          textShadow: '2px 2px 4px rgba(0,0,0,0.5)',
        }}>
          {String(minutes).padStart(2, '0')}:{String(seconds).padStart(2, '0')}
        </div>
      </div>

      {/* –ö–æ–º–ø–∞–∫—Ç–Ω—ã–π —Ç–æ–≤–∞—Ä */}
      <div style={{ 
        display: 'flex', 
        alignItems: 'center', 
        gap: '0.75rem',
        marginBottom: '0.75rem',
      }}>
        <img
          src={specialProduct.imageUrl}
          alt={specialProduct.name}
          style={{ 
            width: '50px', 
            height: '50px', 
            borderRadius: '8px', 
            objectFit: 'cover',
            border: '2px solid #ffd700',
          }}
        />
        <div style={{ flex: 1 }}>
          <div style={{ 
            fontWeight: 'bold', 
            fontSize: '1rem', 
            marginBottom: '0.25rem',
            textShadow: '1px 1px 2px rgba(0,0,0,0.3)'
          }}>
            {specialProduct.name}
          </div>
          <div style={{ 
            display: 'flex', 
            alignItems: 'center', 
            gap: '0.5rem'
          }}>
            <span style={{ 
              textDecoration: 'line-through', 
              fontSize: '0.9rem',
              opacity: 0.8 
            }}>
              {originalPrice} ‚ÇΩ
            </span>
            <span style={{ 
              fontSize: '1.1rem', 
              fontWeight: 'bold',
              color: '#ffff00',
              textShadow: '1px 1px 2px rgba(0,0,0,0.5)'
            }}>
              {discountedPrice} ‚ÇΩ
            </span>
            <span style={{
              background: '#ffff00',
              color: '#ff0844',
              padding: '0.1rem 0.4rem',
              borderRadius: '10px',
              fontSize: '0.7rem',
              fontWeight: 'bold',
            }}>
              -99%
            </span>
          </div>
        </div>
      </div>

      <button
        onClick={() => {
          // –ü—Ä–æ–≤–µ—Ä—è–µ–º, –Ω–µ—Ç –ª–∏ —É–∂–µ —ç—Ç–æ–≥–æ —Ç–æ–≤–∞—Ä–∞ –≤ –∫–æ—Ä–∑–∏–Ω–µ
          const existingFlashItem = cart.find(item => 
            item.id === `${specialProduct.id}_flash`
          );
          
          if (existingFlashItem) {
            // –ï—Å–ª–∏ —É–∂–µ –µ—Å—Ç—å - –Ω–µ –¥–æ–±–∞–≤–ª—è–µ–º –∏ —Å–∫—Ä—ã–≤–∞–µ–º –ø—Ä–µ–¥–ª–æ–∂–µ–Ω–∏–µ
            setIsActive(false);
            return;
          }

          // –î–æ–±–∞–≤–ª—è–µ–º —Ç–æ–≤–∞—Ä —Å–æ —Å–∫–∏–¥–∫–æ–π 99% (—Ç–æ–ª—å–∫–æ 1 —à—Ç—É–∫–∞)
          const discountedProduct = {
            ...specialProduct,
            price: discountedPrice,
            originalPrice: originalPrice,
            isFlashOffer: true,
            quantity: 1, // –§–∏–∫—Å–∏—Ä–æ–≤–∞–Ω–Ω–æ–µ –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ
            name: `${specialProduct.name} ‚ö°`,
            id: `${specialProduct.id}_flash` // –£–Ω–∏–∫–∞–ª—å–Ω—ã–π ID –¥–ª—è flash-–≤–µ—Ä—Å–∏–∏
          };
          addToCart(discountedProduct);
          setIsActive(false); // –°–∫—Ä—ã–≤–∞–µ–º –ø—Ä–µ–¥–ª–æ–∂–µ–Ω–∏–µ –ø–æ—Å–ª–µ –¥–æ–±–∞–≤–ª–µ–Ω–∏—è
        }}
        style={{
          width: '100%',
          padding: '0.6rem',
          background: 'linear-gradient(135deg, #ffff00, #ffd700)',
          color: '#ff0844',
          border: 'none',
          borderRadius: '8px',
          fontSize: '1rem',
          fontWeight: 'bold',
          cursor: 'pointer',
          textTransform: 'uppercase',
          letterSpacing: '0.5px',
          textShadow: '1px 1px 2px rgba(0,0,0,0.3)',
          boxShadow: '0 2px 8px rgba(255, 215, 0, 0.4)',
          transition: 'all 0.2s ease',
        }}
        onMouseEnter={(e) => {
          e.target.style.transform = 'scale(1.02)';
          e.target.style.boxShadow = '0 4px 12px rgba(255, 215, 0, 0.6)';
        }}
        onMouseLeave={(e) => {
          e.target.style.transform = 'scale(1)';
          e.target.style.boxShadow = '0 2px 8px rgba(255, 215, 0, 0.4)';
        }}
      >
        üî• –°–•–í–ê–¢–ò–¢–¨! üî•
      </button>
    </div>
  );
};

export default FlashOfferTimer;
